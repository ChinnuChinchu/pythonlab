# kilometers to miles
kilometers = 5.5
# conversion factor
conv_fac = 0.621371
# calculate miles
miles = kilometers * conv_fac
print('%0.3f kilometers is equal to %0.3f miles ' % (kilometers, miles))

kilometers = float(input('Enter the value:'))
# conversion factor
conv_fac = 0.621371
# calculate miles
miles = (float(kilometers) * conv_fac)
print('%0.3f kilometers is equal to %0.3f miles ' % (kilometers, miles))

